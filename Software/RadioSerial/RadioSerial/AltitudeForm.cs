        LineAnnotation lineAnnotation;
        private void timer1_Tick(object sender, EventArgs e)
        {
            double currentTime = DateTime.Now.Subtract(start).TotalSeconds;
            for (int i = lastAdded + 1; i < testData.Length && testData[i].Item1 <= currentTime; i++)
            {
                chart.Series["Altitude"].Points.AddXY(testData[i].Item1, testData[i].Item2);
                lastAdded = i;
            }
            //maxLine.IntervalOffset = chart.Series["Altitude"].Points.FindMaxByValue().YValues[0];
            DataPoint maxPoint = chart.Series["Altitude"].Points.FindMaxByValue();
            maxAltitudeLabel.Text = maxPoint.YValues[0] + " ft";
            maxAltitudeTimeLabel.Text = maxPoint.XValue + "s";
            if (lineAnnotation == null)
            {
                lineAnnotation = new LineAnnotation()
                {
                    IsSizeAlwaysRelative = false,
                    LineColor = Color.Blue,
                    LineWidth = 2,
                    X = 0,
                    Y = maxPoint.YValues[0],
                    Width = maxPoint.XValue,
                    Height = 0,
                    AnchorDataPoint = chart.Series["Altitude"].Points[0]
                };
                chart.Annotations.Add(lineAnnotation);
            }
            else
            {
                lineAnnotation.Y = maxPoint.YValues[0];
                lineAnnotation.Width = maxPoint.XValue;
            }

            chart.Invalidate();
        }

